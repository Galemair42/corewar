#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	j;

	j = 0;
	i = 0;
	while (s1[i])
		i++;
	while (j < n)
	{
		if (!(s1[i] = s2[j]))
			return (NULL);
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
