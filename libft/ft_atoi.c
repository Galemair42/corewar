#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		result;
	int		sign;

	result = 0;
	i = 0;
	sign = 1;
	while (str[i] == '\t' || str[i] == '\r' || str[i] == '\n' ||
			str[i] == '\v' || str[i] == ' ' || str[i] == '\f')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - '0');
		i++;
	}
	return (sign * result);
}
