#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	c = (n % 10) + 48;
	if (n < 0)
	{
		if (n == -2147483648)
		{
			ft_putnbr(n / 10);
			write(1, "8", 1);
		}
		else
		{
			write(1, "-", 1);
			ft_putnbr(n * -1);
		}
	}
	if (n == 0)
		write(1, "0", 1);
	if (n > 0 && n < 10)
		write(1, &c, 1);
	if (n > 9)
	{
		ft_putnbr(n / 10);
		write(1, &c, 1);
	}
}
